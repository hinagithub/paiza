// ---------------------------------------------------------------
// 問題
// 整数 A = 202、B = 134、C = 107 とします。
// ((A+B)*C)^2を計算した結果を出力してください。
// ---------------------------------------------------------------
package main

import (
	"fmt"
)

func main() {
	n1 := 202
	n2 := 134
	n3 := 107
	answer := (n1 + n2) * n3
	answer = answer * answer
	fmt.Println(answer)
}
